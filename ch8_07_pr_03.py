# this will print everything in new line
print("Hello")
print("how")
print("are")
print("you?")

# coz print has default /n in its end
print("Hello", end="\n")
print("how", end="\n")
print("are", end="\n")
print("you?", end="\n")

# if we put nothing in end, it'll print in one line
print("Hello", end="")
print("how", end="")
print("are", end="")
print("you?", end="")

# if we put space in end, it'll print that with space in between
print("Hello", end=" ")
print("how", end=" ")
print("are", end=" ")
print("you?", end=" ")