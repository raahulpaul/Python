# create a list using []
a = [1,2,4,56,6]

# print the list using print() function
print(a) # It'll print the list as it is..

# access using index via a[0], a[1], a[2] and vice versa
print(a[0]) # It'll print the first letter of list
# print(a[5]) # it'll give error bcoz it exceeds the value of list

# change the value of list using..

